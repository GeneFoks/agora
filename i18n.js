  // ─── Engine ───────────────────────────────────────────────────────────────
  var STORAGE_KEY = 'agora_lang';

  function getLang() {
    return localStorage.getItem(STORAGE_KEY) || 'ru';
  }

  function setLang(lang) {
    localStorage.setItem(STORAGE_KEY, lang);
    applyLang(lang);
    updateSwitcher(lang);
  }

  function t(key) {
    var lang = getLang();
    return (T[lang] && T[lang][key]) || (T['ru'] && T['ru'][key]) || key;
  }

  function applyLang(lang) {
    var dict = T[lang] || T['ru'];

    // <html lang>
    document.documentElement.lang = lang;

    // <title>
    var page = detectPage();
    if (page && dict[page + '.title']) {
      document.title = dict[page + '.title'];
    }

    // <meta description>
    var metaDesc = document.querySelector('meta[name="description"]');
    if (metaDesc && page && dict[page + '.meta']) {
      metaDesc.setAttribute('content', dict[page + '.meta']);
    }

    // text nodes
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var key = el.getAttribute('data-i18n');
      if (dict[key] !== undefined) el.textContent = dict[key];
    });

    // html nodes (allow inline tags like <em>, <strong>, <br>)
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-html');
      if (dict[key] !== undefined) el.innerHTML = dict[key];
    });

    // placeholders
    document.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      var key = el.getAttribute('data-i18n-ph');
      if (dict[key] !== undefined) el.placeholder = dict[key];
    });
  }

  function detectPage() {
    var p = window.location.pathname.split('/').pop().replace('.html', '') || 'index';
    if (p === '') p = 'index';
    var known = ['index', 'about', 'land', 'join', 'map'];
    return known.indexOf(p) !== -1 ? p : null;
  }

  function updateSwitcher(lang) {
    document.querySelectorAll('.lang-btn').forEach(function (btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
  }

  // ─── Expose globally ──────────────────────────────────────────────────────
  window.AgoraI18n = { getLang: getLang, setLang: setLang, t: t, applyLang: applyLang };

  // Auto-apply on DOM ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function () { applyLang(getLang()); });
  } else {
    applyLang(getLang());
  }
